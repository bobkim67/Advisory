def render_taa_tilt_summary_md(
    *,
    as_of_run: str,
    etf_payload: dict[str, Any],
    fund_payload: dict[str, Any],
    etf_png_rel: str,
    fund_png_rel: str,
    out_path: Path,
) -> Path:
    lines: list[str] = []
    lines.append(f"# TAA Regime Tilt Summary ({as_of_run})")
    lines.append("")
    lines.append(f"> schema_version: {SCHEMA_VERSION}")
    lines.append(f"> {LIMITATION_TEXT}")
    lines.append("")
    for label, payload, png_rel in (
        ("ETF", etf_payload, etf_png_rel),
        ("Fund", fund_payload, fund_png_rel),
    ):
        meta = payload["meta"]
        rc = payload["regime_context"]
        ba = payload["portfolio_before_after"]
        delta = ba["delta"]
        rules = payload["tilt_rules_applied"]
        lines.append(f"## {label}")
        lines.append("")
        lines.append(
            f"- regime: **R{rc['current_regime']} {rc['quadrant_label']}**, "
            f"P={rc['placement']:+.4f}, V={rc['velocity']:+.4f}"
        )
        lines.append(
            f"- portfolio as_of={meta['portfolio_as_of_date']}, "
            f"regime signal as_of={meta['regime_signal_as_of_date']}"
        )
        lines.append(
            f"- SAA: E[R]={ba['saa']['expected_return'] * 100:.2f}%, "
            f"σ={ba['saa']['volatility'] * 100:.2f}%, Sharpe={ba['saa']['sharpe']:.4f}"
        )
        lines.append(
            f"- TAA: E[R]={ba['taa_target']['expected_return'] * 100:.2f}%, "
            f"σ={ba['taa_target']['volatility'] * 100:.2f}%, "
            f"Sharpe={ba['taa_target']['sharpe']:.4f}"
        )
        lines.append(
            f"- Δ: E[R]={delta['expected_return'] * 100:+.2f}pp, "
            f"σ={delta['volatility'] * 100:+.2f}pp, "
            f"Sharpe={delta['sharpe']:+.4f}"
        )
        lines.append("- Applied tilts:")
        for r in rules:
            if abs(r["applied_tilt_pp"]) < 1e-6:
                continue
            sign = "+" if r["applied_tilt_pp"] > 0 else ""
            lines.append(
                f"  - {r['asset_key']}: SAA {r['saa_weight'] * 100:.2f}% "
                f"→ TAA {r['taa_target_weight'] * 100:.2f}% "
                f"(tilt {sign}{r['applied_tilt_pp']:.2f}pp, {r['direction']})"
            )
        lines.append("")
        lines.append(f"![{label} TAA Tilt]({png_rel})")
        lines.append("")
    lines.append("---")
    lines.append("")
    lines.append(f"**Limitation**: {LIMITATION_TEXT}")
    lines.append("")
    out_path = Path(out_path)
    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text("\n".join(lines), encoding="utf-8")
    return out_path


