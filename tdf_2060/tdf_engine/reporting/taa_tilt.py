    gs = _gs.GridSpec(
        nrows=6, ncols=2,
        height_ratios=[0.65, 1.4, 1.6, 3.0, 3.2, 0.55],
        width_ratios=[1.0, 1.0],
        hspace=0.55, wspace=0.20,
        left=0.07, right=0.97, top=0.97, bottom=0.04,
    )

    # 1. Header
    ax_h = fig.add_subplot(gs[0, :])
    ax_h.set_axis_off()
    ax_h.text(
        0.0, 0.85,
        f"TAA Regime Tilt — {pt} Portfolio  ·  Rule-Based Overlay Diagnostic",
        transform=ax_h.transAxes,
        fontsize=15, fontweight="bold", va="top",
    )
    ax_h.text(
        0.0, 0.45,
        f"portfolio as_of={meta.get('portfolio_as_of_date')}  ·  "
        f"regime signal as_of={meta.get('regime_signal_as_of_date')}  ·  "
        f"current regime={rc.get('current_regime')} "
        f"({rc.get('current_regime_label')})",
        transform=ax_h.transAxes, fontsize=10.5, color="#333", va="center",
    )
    ax_h.text(
        0.0, 0.10,
        f"method = {pol.get('method')}  ·  policy = {pol.get('policy_id')}  ·  "
        f"version = {pol.get('policy_version')}",
        transform=ax_h.transAxes, fontsize=9.5, color="#7a3a3a", va="bottom",
    )

    # 2. Regime context (left card + right reference)
    ax_rc = fig.add_subplot(gs[1, 0])
    ax_rc.set_axis_off()
    ax_rc.text(
        0.02, 0.92, "Regime Context",
        fontsize=11, fontweight="bold", transform=ax_rc.transAxes,
    )
    ax_rc.text(
        0.02, 0.74,
        f"R{rc.get('current_regime')}  {rc.get('quadrant_label')}",
        fontsize=11, transform=ax_rc.transAxes,
    )
    ax_rc.text(
        0.02, 0.55,
        f"Placement : {float(rc.get('placement') or 0):+.4f}",
        fontsize=10, transform=ax_rc.transAxes,
    )
    ax_rc.text(
        0.02, 0.40,
        f"Velocity  : {float(rc.get('velocity') or 0):+.4f}",
        fontsize=10, transform=ax_rc.transAxes,
    )
    ax_rc.text(
        0.02, 0.20,
        f"signal as_of: {meta.get('regime_signal_as_of_date')}",
        fontsize=10, color="#555", transform=ax_rc.transAxes,
    )
    ax_rc.text(
        0.02, 0.04,
        "Full trajectory: see E-8 regime_clock_*.png",
        fontsize=9, color="#888", style="italic", transform=ax_rc.transAxes,
    )

    # mini placement-velocity card (current point only — full clock 은 E-8 참조)
    ax_pv = fig.add_subplot(gs[1, 1])
    p = float(rc.get("placement") or 0.0)
    v = float(rc.get("velocity") or 0.0)
    pmax = max(abs(p), 0.05) * 1.6
    vmax = max(abs(v), 0.05) * 1.6
    ax_pv.fill_betweenx([0, vmax], 0, pmax, color="#3a8a3e", alpha=0.07)
    ax_pv.fill_betweenx([0, vmax], -pmax, 0, color="#3a6db0", alpha=0.07)
    ax_pv.fill_betweenx([-vmax, 0], -pmax, 0, color="#a83232", alpha=0.07)
    ax_pv.fill_betweenx([-vmax, 0], 0, pmax, color="#c97a2a", alpha=0.07)
    ax_pv.axhline(0, color="#444", linewidth=0.6)
    ax_pv.axvline(0, color="#444", linewidth=0.6)
    ax_pv.scatter([p], [v], s=240, marker="*", color="#222",
                  edgecolors="white", linewidths=1.4, zorder=5)
    ax_pv.set_xlim(-pmax, pmax)
    ax_pv.set_ylim(-vmax, vmax)
    ax_pv.set_xlabel("Placement", fontsize=9)
    ax_pv.set_ylabel("Velocity", fontsize=9)
    ax_pv.set_title(
        "current point only (full clock → E-8)",
        fontsize=9, color="#555", loc="center",
    )
    ax_pv.tick_params(labelsize=8)

    # 3. Tilt policy panel
    ax_pol = fig.add_subplot(gs[2, :])
    ax_pol.set_axis_off()
    ax_pol.text(
        0.0, 0.92, "Rule-Based Tilt Policy (current regime)",
        transform=ax_pol.transAxes, fontsize=12, fontweight="bold",
    )
