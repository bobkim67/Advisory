def build_taa_tilt(
    portfolio: dict[str, Any],
    *,
    taa_policy: dict[str, Any] | None = None,
) -> dict[str, Any]:
    """portfolio + (옵션) taa_policy.yaml → TAA tilt diagnostic dict.

    raises ValueError if direct SAA / TAA feasibility / CMA telemetry missing.
    """
    saa_diag, taa_diag, cma = _require_taa_inputs(portfolio)
    feas = taa_diag.get("taa_feasibility") or {}
    regime = (portfolio.get("diagnostics") or {}).get("regime") or {}

    saa_w_dict = {str(k): float(v) for k, v in saa_diag["saa_weights"].items()}
    taa_w_dict = {
        str(k): float(v)
        for k, v in (feas.get("target_weights_before_projection") or {}).items()
    }
    keys = _ordered_keys(cma, saa_w_dict)
    er_v = _vec(cma["expected_returns"], keys)
    cov = _mat(cma["covariance_matrix"], keys)
    rf = float(saa_diag.get("rf") or 0.0)

    saa_v = _vec(saa_w_dict, keys)
    taa_v = _vec(taa_w_dict, keys)
    tilt_v = [taa_v[i] - saa_v[i] for i in range(len(keys))]

    # before / after metrics
    saa_ret, saa_vol, saa_sharpe = _portfolio_metrics(saa_v, er_v, cov, rf)
    taa_ret, taa_vol, taa_sharpe = _portfolio_metrics(taa_v, er_v, cov, rf)
    delta_sharpe = (taa_sharpe - saa_sharpe) if (
        math.isfinite(saa_sharpe) and math.isfinite(taa_sharpe)
    ) else None

    # asset_level_effect
    sigma_w_taa = _matvec(cov, taa_v)
    by_asset_effect: dict[str, dict[str, float]] = {}
    for i, ak in enumerate(keys):
        by_asset_effect[ak] = {
            "saa_weight": float(saa_v[i]),
            "tilt": float(tilt_v[i]),
            "taa_target_weight": float(taa_v[i]),
            "marginal_expected_return_effect": float(er_v[i] * tilt_v[i]),
            "marginal_risk_effect": float(sigma_w_taa[i] * tilt_v[i]),
            "contribution_to_delta_return": float(er_v[i] * tilt_v[i]),
        }

    # tilt_policy block (taa_policy.yaml::regime_tilts.<regime_id>)
    regime_id = int(regime.get("regime") or 0)
    policy_block: dict[str, Any] = {
        "policy_id": "taa_policy.yaml::regime_tilts",
        "policy_version": "phase-d frozen",
        "method": METHOD_LABEL,
        "description": (
            "Rule-based regime overlay. SAA + asset_tilts (per-regime fixed) "
            "→ TAA target; tilt_sum=0 정합. Not regime-conditioned MVO."
        ),
        "limitation_text": LIMITATION_TEXT,
    }
    regime_reason = ""
    asset_tilts_yaml: dict[str, float] = {}
    if taa_policy:
        regime_tilts = (taa_policy.get("regime_tilts") or {}).get(regime_id) or {}
        asset_tilts_yaml = regime_tilts.get("asset_tilts") or {}
        regime_reason = str(regime_tilts.get("reason") or "")
        policy_block["regime_label_yaml"] = str(regime_tilts.get("label") or "")
        policy_block["regime_reason"] = regime_reason

    # tilt_rules_applied — non-zero tilts (from yaml policy + applied tilts)
    tilt_rules: list[dict[str, Any]] = []
    for ak, w_raw in sorted(asset_tilts_yaml.items()):
        policy_tilt_pp = float(w_raw) * 100.0
        applied_tilt_pp = (taa_w_dict.get(ak, 0.0) - saa_w_dict.get(ak, 0.0)) * 100.0
        direction = (
            "overweight" if applied_tilt_pp > 1e-6
            else "underweight" if applied_tilt_pp < -1e-6
            else "neutral"
        )
        tilt_rules.append({
            "asset_key": ak,
            "saa_weight": float(saa_w_dict.get(ak, 0.0)),
            "tilt": float((applied_tilt_pp) / 100.0),
            "taa_target_weight": float(taa_w_dict.get(ak, 0.0)),
            "policy_tilt_pp": policy_tilt_pp,
            "applied_tilt_pp": applied_tilt_pp,
            "direction": direction,
            "rationale": (
                f"Regime {regime_id} → asset_tilts.{ak} = {policy_tilt_pp:+.2f}pp "
                f"(applied {applied_tilt_pp:+.2f}pp)"
            ),
            "source": METHOD_LABEL,
            "confidence": None,
        })

    # regime_context — 외부 E-8 산출물 path 만 reference (별도 첨부 안 함)
    payload: dict[str, Any] = {
        "meta": {
            "schema_version": SCHEMA_VERSION,
            "product_type": str(portfolio.get("portfolio_type") or ""),
            "portfolio_as_of_date": str(portfolio.get("as_of_date") or ""),
            "regime_signal_as_of_date": str(regime.get("as_of") or ""),
            "source_mode": str(portfolio.get("source_type") or "file"),
            "generated_at": datetime.now(timezone.utc).isoformat(),
        },
        "regime_context": {
            "current_regime": regime_id,
            "current_regime_label": str(regime.get("regime_label") or ""),
            "placement": float(regime.get("placement") or 0.0),
            "velocity": float(regime.get("velocity") or 0.0),
            "quadrant_label": REGIME_QUADRANT.get(regime_id, "Unknown"),
            "recent_transition_summary": (
                "history detail in E-8 sidecar regime_history_*.json"
            ),
            "regime_clock_reference": {
                "source_json": (
                    "out/db_review_relaxed_e62/regime_history/<as_of>/"
                    "regime_history_<pt>_<as_of>.json"
                ),
                "source_png": (
                    "out/db_review_relaxed_e62/regime_history/<as_of>/"
                    "regime_clock_<pt>_<as_of>.png"
                ),
            },
        },
        "tilt_policy": policy_block,
        "tilt_rules_applied": tilt_rules,
        "portfolio_before_after": {
            "saa": {
                "expected_return": float(saa_ret),
                "volatility": float(saa_vol),
                "sharpe": (float(saa_sharpe) if math.isfinite(saa_sharpe) else None),
                "weights": saa_w_dict,
            },
            "taa_target": {
                "expected_return": float(taa_ret),
                "volatility": float(taa_vol),
                "sharpe": (float(taa_sharpe) if math.isfinite(taa_sharpe) else None),
                "weights": taa_w_dict,
            },
            "delta": {
                "expected_return": float(taa_ret - saa_ret),
                "volatility": float(taa_vol - saa_vol),
                "sharpe": (float(delta_sharpe) if delta_sharpe is not None else None),
            },
            "label": "Diagnostic before/after comparison (rule-based overlay)",
        },
        "asset_level_effect": {
            "by_asset": by_asset_effect,
            "delta_return_check_sum": float(taa_ret - saa_ret),
        },
        "diagnostics": {
            "warnings": [],
            "missing_data": [
                {
                    "field": "regime_conditioned_assumptions",
                    "impact": "regime-aware MVO 비교 불가",
                    "recommended_next_step": (
                        "future phase — regime_mvo (currently future_study only)"
                    ),
                },
                {
                    "field": "tilt_rules_applied[].confidence",
                    "impact": "tilt 의 통계적 유의성 표시 불가",
                    "recommended_next_step": "future phase — confidence scaling",
                },
            ],
        },
    }
    return payload


def write_taa_tilt_json(payload: dict[str, Any], out_path: Path) -> Path:
    out_path = Path(out_path)
    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text(
        json.dumps(payload, ensure_ascii=False, indent=2, default=str),
        encoding="utf-8",
    )
    return out_path


