def _require_taa_inputs(portfolio: dict[str, Any]) -> tuple[dict, dict, dict]:
    diag = portfolio.get("diagnostics") or {}
    saa = diag.get("saa_diagnostics") or {}
    taa = diag.get("taa_diagnostics") or {}
    cma = saa.get("cma") or {}
    if not saa.get("saa_weights"):
        raise ValueError(
            "E-10 TAA tilt requires direct SAA telemetry "
            "`diagnostics.saa_diagnostics.saa_weights` (E-6.2 T-6). "
            "Inferred SAA is forbidden."
        )
    feas = taa.get("taa_feasibility") or {}
    if not feas.get("target_weights_before_projection"):
        raise ValueError(
            "E-10 TAA tilt requires "
            "`diagnostics.taa_diagnostics.taa_feasibility.target_weights_before_projection`."
        )
    for need in ("expected_returns", "covariance_matrix"):
        if not cma.get(need):
            raise ValueError(
                f"E-10 TAA tilt requires `saa_diagnostics.cma.{need}` (E-6.2 T-1~T-4)."
            )
    return saa, taa, cma


# ---------------------------------------------------------------------------
# Numerical helpers
# ---------------------------------------------------------------------------


def _ordered_keys(cma: dict[str, Any], fallback: dict[str, float]) -> list[str]:
    keys = cma.get("asset_keys") or list(fallback.keys())
    return [str(k) for k in keys]


def _vec(d: dict[str, float], keys: list[str]) -> list[float]:
    return [float(d.get(k, 0.0)) for k in keys]


def _mat(d: dict[str, dict[str, float]], keys: list[str]) -> list[list[float]]:
    return [
        [float((d.get(ki) or {}).get(kj, 0.0)) for kj in keys]
        for ki in keys
    ]


def _matvec(mat: list[list[float]], v: list[float]) -> list[float]:
    n = len(v)
    out = [0.0] * n
    for i in range(n):
        s = 0.0
        row = mat[i]
        for j in range(n):
            s += row[j] * v[j]
        out[i] = s
    return out


def _portfolio_metrics(
    weights: list[float], er: list[float], cov: list[list[float]], rf: float
) -> tuple[float, float, float]:
    sigma_w = _matvec(cov, weights)
    var = sum(weights[i] * sigma_w[i] for i in range(len(weights)))
    var = max(var, 0.0)
    vol = math.sqrt(var)
    ret = sum(weights[i] * er[i] for i in range(len(weights)))
    sharpe = (ret - rf) / vol if vol > 1e-12 else float("nan")
    return ret, vol, sharpe


