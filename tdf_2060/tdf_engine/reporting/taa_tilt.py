    uw_list = [r for r in rules if r["direction"] == "underweight"]
    ow_text = (
        "  ".join([f"{r['asset_key']} {r['policy_tilt_pp']:+.2f}pp" for r in ow_list])
        or "(none)"
    )
    uw_text = (
        "  ".join([f"{r['asset_key']} {r['policy_tilt_pp']:+.2f}pp" for r in uw_list])
        or "(none)"
    )
    ax_pol.text(
        0.02, 0.66,
        f"Overweight  : {ow_text}",
        transform=ax_pol.transAxes, fontsize=10, color="#2a8a3e",
    )
    ax_pol.text(
        0.02, 0.42,
        f"Underweight : {uw_text}",
        transform=ax_pol.transAxes, fontsize=10, color="#a83232",
    )
