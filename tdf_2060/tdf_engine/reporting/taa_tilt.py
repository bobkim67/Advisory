def _select_korean_font() -> str | None:
    import matplotlib.font_manager as fm

    candidates = ("Malgun Gothic", "AppleGothic", "NanumGothic", "Gulim", "MS Gothic")
    available = {f.name for f in fm.fontManager.ttflist}
    for name in candidates:
        if name in available:
            return name
    return None


def render_taa_tilt(
    payload: dict[str, Any],
    out_path: Path,
    *,
    label: str | None = None,
) -> Path:
    """6-panel TAA PNG. read-only (deepcopy)."""
    payload = deepcopy(payload)

    import matplotlib

    matplotlib.use("Agg")
    import matplotlib.pyplot as plt
    from matplotlib import gridspec as _gs
    import numpy as np

    font_name = _select_korean_font()
    if font_name:
        plt.rcParams["font.family"] = [font_name, "DejaVu Sans"]
    plt.rcParams["axes.unicode_minus"] = False

    meta = payload["meta"]
    rc = payload["regime_context"]
    pol = payload["tilt_policy"]
    rules = payload["tilt_rules_applied"]
    ba = payload["portfolio_before_after"]
    saa = ba["saa"]
    taa = ba["taa_target"]
    delta = ba["delta"]

    pt = (meta.get("product_type") or "").upper()

